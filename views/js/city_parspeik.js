function cityList(province_id) {
    remove_select_options('id_city');
    var select_box = document.getElementById('id_city');
    select_box.innerHTML = "";
    select_box.options.add(new Option('شهر را انتخاب نمایید ...', 0));
    var selected_province = p24_city_list[province_id];
    for (var id in selected_province) select_box.options.add(new Option(selected_province[id].n, selected_province[id].i));
}

function remove_select_options(selectbox_id) {
    var select_box = document.getElementById(selectbox_id);
    var l = select_box.options.length;
    if (l > 0) {
        for (var i = 0; i <= l; i++) select_box.options.remove(i);
    }
    return true;
}

$(document).ready(function () {
    var select_box = document.getElementById("id_state");
    if (select_box.value != 0) cityList(select_box.value);
});